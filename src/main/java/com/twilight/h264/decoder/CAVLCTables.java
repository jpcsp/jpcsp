	public static short[][] expandTable(short[][] input) {
		int tableSize = 0;
		// Calculate Table Size.
		for(int i=0;i<input.length;i++)
			tableSize += input[i][2];
		short[][] output = new short[tableSize][2];
		int j = 0;
		for(int i=0;i<input.length;i++) {
			for(int k=0;k<input[i][2];k++) {
				output[j+k][0] = input[i][0];
				output[j+k][1] = input[i][1];
			} // for k
			j += input[i][2];
		} // for i
		return output;
	}
	
